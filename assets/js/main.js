// Function: Generate Password
const generatePasswd = () => {
  let upperCaseCB, lowerCaseCB, numbersCB, randomCharactersCB = false;
  let randomPasswd = [];
  let passLength = inputPasswordRange.value;
  let myAsciiCodes = {
    100: "A", 101: "B", 102: "C", 103: "D", 104: "E",
    105: "F", 106: "G", 107: "H", 108: "I", 109: "J",
    110: "K", 111: "L", 112: "M", 113: "N", 114: "O",
    115: "P", 116: "Q", 117: "R", 118: "S", 119: "T",
    120: "U", 121: "V", 122: "W", 123: "X", 124: "Y",
    125: "Z", 126: "a", 127: "b", 128: "c", 129: "d",
    130: "e", 131: "f", 132: "g", 133: "h", 134: "i",
    135: "j", 136: "k", 137: "l", 138: "m", 139: "n",
    140: "o", 141: "p", 142: "q", 143: "r", 144: "s",
    145: "t", 146: "u", 147: "v", 148: "w", 149: "x",
    150: "y", 151: "z", 152: "0", 153: "1", 154: "2",
    155: "3", 156: "4", 157: "5", 158: "6", 159: "7",
    160: "8", 161: "9", 162: "@", 163: "#", 164: "$",
    165: "%", 166: "^", 167: "&", 168: "*", 169: "(",
    170: ")", 171: "-", 172: "_", 173: "=", 174: "+",
    175: "[", 176: "]", 177: "{", 178: "}", 179: "|",
    180: ";", 181: ":", 182: "'", 183: "\"", 184: "<",
    185: ">", 186: ",", 187: ".", 188: "?", 189: "/",
    190: "`", 191: "~", 192: "!", 193: "€"
  };
  /*
      ABC: 100 - 125
      abc: 126 - 151
      123: 152 - 161
      %#^%: 162 - 193
  */

  document.querySelector('#upperCaseCB:checked') ? upperCaseCB = true : upperCaseCB = false;
  document.querySelector('#lowerCaseCB:checked') ? lowerCaseCB = true : lowerCaseCB = false;
  document.querySelector('#numbersCB:checked') ? numbersCB = true : numbersCB = false;
  document.querySelector('#randomCharactersCB:checked') ? randomCharactersCB = true : randomCharactersCB = false;

  while (passLength > 0) {
    let randomPasswdCode = Math.trunc(Math.random() * 1000);
    if (randomPasswdCode >= 100 && randomPasswdCode <= 193) {
      // ABC
      if (randomPasswdCode >= 100 && randomPasswdCode <= 125 && upperCaseCB) {
        randomPasswd.push(myAsciiCodes[randomPasswdCode]);
        passLength--;
      }
      // abc
      else if (randomPasswdCode >= 126 && randomPasswdCode <= 151 && lowerCaseCB) {
        randomPasswd.push(myAsciiCodes[randomPasswdCode]);
        passLength--;
      }
      // 123
      else if (randomPasswdCode >= 152 && randomPasswdCode <= 161 && numbersCB) {
        randomPasswd.push(myAsciiCodes[randomPasswdCode]);
        passLength--;
      }
      // %#^%
      else if (randomPasswdCode >= 162 && randomPasswdCode <= 193 && randomCharactersCB) {
        randomPasswd.push(myAsciiCodes[randomPasswdCode]);
        passLength--;
      }
    }
  }

  passwdGenTextInput.value = randomPasswd.join('');
}


