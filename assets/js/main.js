// Function; Set Theme
const setTheme = (trueOrFalse) => {
  if (trueOrFalse) {
    document.body.classList.toggle("dark-theme");
    localStorage.setItem("theme", "dark");
    document.getElementById("themeSettings").checked = true;
  }
  else {
    document.body.classList.remove("dark-theme");
    localStorage.setItem("theme", "light");
    document.getElementById("themeSettings").checked = false;
  }
}

