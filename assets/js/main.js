// Function: Security Status
const setSecurityStatus = (securityValue) => {
  if (securityValue <= 6) {
    passwdSafetyInformation.style.backgroundColor = "#ffdc9d";
    passwdSafetyInformation.innerHTML = "Weak";
  }
  else if (securityValue >= 7 && securityValue <= 9) {
    passwdSafetyInformation.style.background = "#fafa8a";
    passwdSafetyInformation.innerHTML = "Good";
  }
  else if (securityValue >= 10 && securityValue <= 13) {
    passwdSafetyInformation.style.background = "#c5f9c5";
    passwdSafetyInformation.innerHTML = "Strong";
  }
  else if (securityValue >= 14) {
    passwdSafetyInformation.style.background = "#90EE90";
    passwdSafetyInformation.innerHTML = "Very Strong";
  }
}

